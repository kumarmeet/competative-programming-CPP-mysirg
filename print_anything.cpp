#include<bits/stdc++.h>
class Print {
public:
    void display(int n) {
        cout<<n<<endl;
    }
    void display(long int i) {
        cout<<i<<endl;
    }
    void display(char ch) {
        cout<<ch<<endl;
    }
    void display(string s) {
        cout<<s<<endl;
    }
    void display(double d) {
        cout<<setprecision(13)<<d<<endl;
    }
    void display(float f) {
        cout<<f<<endl;
    }
};
